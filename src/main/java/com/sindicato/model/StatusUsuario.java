package com.sindicato.model;

/**
 * Enum que representa os possíveis status de um usuário no sistema.
 */
public enum StatusUsuario {
    /**
     * Usuário ativo e com permissão para acessar o sistema
     */
    ATIVO,
    
    /**
     * Usuário inativo, sem permissão para acessar o sistema
     */
    INATIVO
}
